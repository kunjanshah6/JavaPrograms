
public class CheckEvenOdd {
	public static void main(String args[])
	{
		//static input
		int number=11;
		
		//checking condition
		if(number%2==0)
		{
			System.out.println(number + " "+ "is Even Number");
		}
		else {
			System.out.println(number + " "+ "is Odd Number");
		}
	}

}
